export class County {

    
    countyCode: string;
    countyName: string;
    countyContactPhone: string;
    countyEmail: string;

    constructor(countyCode:string,countyName:string,countyContactPhone:string,countyEmail:string)
    {

        this.countyCode = countyCode;
        this.countyName = countyName;
        this.countyContactPhone = countyContactPhone;
        this.countyEmail = countyEmail;
    }
    
        
}
